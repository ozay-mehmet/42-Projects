void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	unsigned int	i;

	i = 0;
	if (!s || !f)
		return ;
	while (s[i] && s && f)
	{
		f(i, &s[i]);
		i++;
	}
}
