#include "push_swap.h"

void	ft_pa(t_stack **a, t_stack **b, t_bench *bench)
{
	t_stack	*tmp;

	if (!b || !*b)
		return ;
	tmp = *b;
	*b = (*b)->next;
	tmp->next = *a;
	*a = tmp;
	ft_print_op("pa", bench);
}

void	ft_pb(t_stack **a, t_stack **b, t_bench *bench)
{
	t_stack	*tmp;

	if (!a || !*a)
		return ;
	tmp = *a;
	*a = (*a)->next;
	tmp->next = *b;
	*b = tmp;
	ft_print_op("pb", bench);
}
